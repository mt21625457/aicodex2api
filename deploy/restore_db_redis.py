#!/usr/bin/env python3
"""Restore PostgreSQL and Redis from tar.zst backup archive.

This script is designed for archives generated by deploy/backup_db_redis.py.

Examples:
  python3 deploy/restore_db_redis.py -a deploy/backups/20260304_120000.tar.zst --dry-run
  python3 deploy/restore_db_redis.py -a deploy/backups/20260304_120000.tar.zst --force
  python3 deploy/restore_db_redis.py -a deploy/backups/20260304_120000.tar.zst --no-redis --force
"""

from __future__ import annotations

import argparse
import importlib.util
import json
import os
import shlex
import subprocess
import sys
import tempfile
from pathlib import Path, PurePosixPath
from typing import Any, Dict, List, Optional

try:
    import backup_db_redis as common
except ModuleNotFoundError:
    helper_path = Path(__file__).resolve().parent / "backup_db_redis.py"
    spec = importlib.util.spec_from_file_location("backup_db_redis", helper_path)
    if spec is None or spec.loader is None:
        raise RuntimeError("failed to load backup_db_redis helper module")
    common = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(common)


BackupError = common.BackupError


def safe_archive_member(member: str) -> str:
    value = str(member).strip()
    while value.startswith("./"):
        value = value[2:]
    if not value:
        raise BackupError("无效的归档成员路径（空值）")
    path = PurePosixPath(value)
    if path.is_absolute() or ".." in path.parts:
        raise BackupError(f"归档成员路径不安全: {member}")
    return str(path)


def run_archive_pipeline(
    archive_file: Path,
    tar_args: List[str],
    *,
    text_output: bool = False,
) -> subprocess.CompletedProcess:
    common.ensure_tool_exists("zstd")
    common.ensure_tool_exists("tar")
    zstd_cmd = ["zstd", "-dc", str(archive_file)]
    tar_cmd = ["tar"] + tar_args

    with subprocess.Popen(zstd_cmd, stdout=subprocess.PIPE, stderr=subprocess.PIPE) as zstd_proc:
        if zstd_proc.stdout is None:
            raise BackupError("zstd 输出管道创建失败")
        tar_proc = subprocess.run(
            tar_cmd,
            stdin=zstd_proc.stdout,
            capture_output=True,
            text=text_output,
            check=False,
        )
        zstd_proc.stdout.close()
        zstd_rc = zstd_proc.wait()
        zstd_stderr = zstd_proc.stderr.read().decode("utf-8", errors="ignore") if zstd_proc.stderr else ""

    if tar_proc.returncode != 0 or zstd_rc != 0:
        tar_stderr = tar_proc.stderr if text_output else tar_proc.stderr.decode("utf-8", errors="ignore")
        parts: List[str] = []
        if zstd_rc != 0:
            parts.append(f"zstd 失败({zstd_rc}): {zstd_stderr.strip()}")
        if tar_proc.returncode != 0:
            parts.append(f"tar 失败({tar_proc.returncode}): {tar_stderr.strip()}")
        raise BackupError("归档读取失败: " + " | ".join(parts))
    return tar_proc


def list_archive_members(archive_file: Path) -> List[str]:
    proc = run_archive_pipeline(archive_file, ["-tf", "-"], text_output=True)
    members = [line.strip() for line in proc.stdout.splitlines() if line.strip()]
    return [safe_archive_member(item) for item in members]


def find_metadata_member(members: List[str]) -> str:
    candidates = [item for item in members if item.endswith("/backup_meta.json") or item == "backup_meta.json"]
    if not candidates:
        raise BackupError("归档中未找到 backup_meta.json")
    candidates.sort()
    return candidates[-1]


def read_archive_member_bytes(archive_file: Path, member: str) -> bytes:
    safe_member = safe_archive_member(member)
    proc = run_archive_pipeline(archive_file, ["-xOf", "-", safe_member], text_output=False)
    return proc.stdout


def read_archive_member_json(archive_file: Path, member: str) -> Dict[str, Any]:
    raw = read_archive_member_bytes(archive_file, member)
    try:
        return json.loads(raw.decode("utf-8"))
    except json.JSONDecodeError as exc:
        raise BackupError(f"归档成员不是有效 JSON: {member}") from exc


def derive_backup_dir_name(meta: Dict[str, Any], metadata_member: str) -> str:
    backup_dir = str(meta.get("backup_dir") or "").strip()
    if backup_dir:
        name = Path(backup_dir).name
        if name:
            return name
    member_path = PurePosixPath(metadata_member)
    if len(member_path.parts) >= 2:
        return member_path.parts[0]
    raise BackupError("无法推断备份目录名")


def resolve_data_member(meta_section: Dict[str, Any], backup_dir_name: str) -> Optional[str]:
    if not meta_section:
        return None
    raw = str(meta_section.get("backup_archive_member") or meta_section.get("backup_file") or "").strip()
    if not raw:
        return None

    candidate = raw
    while candidate.startswith("./"):
        candidate = candidate[2:]
    posix = PurePosixPath(candidate)
    if posix.is_absolute():
        candidate = f"{backup_dir_name}/{Path(candidate).name}"
    elif len(posix.parts) == 1:
        candidate = f"{backup_dir_name}/{candidate}"
    return safe_archive_member(candidate)


def extract_archive_member_to_file(
    archive_file: Path,
    member: str,
    out_file: Path,
    dry_run: bool,
) -> None:
    safe_member = safe_archive_member(member)
    zstd_cmd = ["zstd", "-dc", str(archive_file)]
    tar_cmd = ["tar", "-xOf", "-", safe_member]
    if dry_run:
        print(f"[dry-run] {common.cmd_to_log_string(zstd_cmd)} | {common.cmd_to_log_string(tar_cmd)} > {out_file}")
        return

    common.ensure_tool_exists("zstd")
    common.ensure_tool_exists("tar")
    out_file.parent.mkdir(parents=True, exist_ok=True)

    with out_file.open("wb") as fp:
        with subprocess.Popen(zstd_cmd, stdout=subprocess.PIPE, stderr=subprocess.PIPE) as zstd_proc:
            if zstd_proc.stdout is None:
                raise BackupError("zstd 输出管道创建失败")
            tar_proc = subprocess.run(
                tar_cmd,
                stdin=zstd_proc.stdout,
                stdout=fp,
                stderr=subprocess.PIPE,
                check=False,
            )
            zstd_proc.stdout.close()
            zstd_rc = zstd_proc.wait()
            zstd_stderr = zstd_proc.stderr.read().decode("utf-8", errors="ignore") if zstd_proc.stderr else ""

    if tar_proc.returncode != 0 or zstd_rc != 0:
        if out_file.exists():
            out_file.unlink()
        tar_stderr = tar_proc.stderr.decode("utf-8", errors="ignore")
        parts: List[str] = []
        if zstd_rc != 0:
            parts.append(f"zstd 失败({zstd_rc}): {zstd_stderr.strip()}")
        if tar_proc.returncode != 0:
            parts.append(f"tar 失败({tar_proc.returncode}): {tar_stderr.strip()}")
        raise BackupError("提取归档成员失败: " + " | ".join(parts))


def stream_archive_member_to_cmd(
    archive_file: Path,
    member: str,
    cmd: List[str],
    *,
    env: Optional[Dict[str, str]] = None,
    dry_run: bool,
) -> None:
    safe_member = safe_archive_member(member)
    zstd_cmd = ["zstd", "-dc", str(archive_file)]
    tar_cmd = ["tar", "-xOf", "-", safe_member]
    if dry_run:
        print(f"[dry-run] {common.cmd_to_log_string(zstd_cmd)} | {common.cmd_to_log_string(tar_cmd)} | {common.cmd_to_log_string(cmd)}")
        return

    common.ensure_tool_exists("zstd")
    common.ensure_tool_exists("tar")

    with subprocess.Popen(zstd_cmd, stdout=subprocess.PIPE, stderr=subprocess.PIPE) as zstd_proc:
        if zstd_proc.stdout is None:
            raise BackupError("zstd 输出管道创建失败")
        with subprocess.Popen(tar_cmd, stdin=zstd_proc.stdout, stdout=subprocess.PIPE, stderr=subprocess.PIPE) as tar_proc:
            zstd_proc.stdout.close()
            if tar_proc.stdout is None:
                raise BackupError("tar 输出管道创建失败")
            target_proc = subprocess.run(
                cmd,
                stdin=tar_proc.stdout,
                stderr=subprocess.PIPE,
                env=env,
                check=False,
            )
            tar_proc.stdout.close()
            tar_stderr = tar_proc.stderr.read().decode("utf-8", errors="ignore") if tar_proc.stderr else ""
            tar_rc = tar_proc.wait()
        zstd_rc = zstd_proc.wait()
        zstd_stderr = zstd_proc.stderr.read().decode("utf-8", errors="ignore") if zstd_proc.stderr else ""

    if zstd_rc != 0 or tar_rc != 0 or target_proc.returncode != 0:
        target_stderr = target_proc.stderr.decode("utf-8", errors="ignore")
        parts: List[str] = []
        if zstd_rc != 0:
            parts.append(f"zstd 失败({zstd_rc}): {zstd_stderr.strip()}")
        if tar_rc != 0:
            parts.append(f"tar 失败({tar_rc}): {tar_stderr.strip()}")
        if target_proc.returncode != 0:
            parts.append(f"目标命令失败({target_proc.returncode}): {target_stderr.strip()}")
        raise BackupError("恢复失败: " + " | ".join(parts))


def restore_postgres_in_container(
    archive_file: Path,
    member: str,
    container_id: str,
    db_user: str,
    db_password: str,
    db_name: str,
    postgres_clean: bool,
    dry_run: bool,
) -> None:
    cmd = ["docker", "exec", "-i"]
    if db_password:
        cmd.extend(["-e", f"PGPASSWORD={db_password}"])
    cmd.extend(
        [
            container_id,
            "pg_restore",
            "-U",
            db_user,
            "-d",
            db_name,
            "--no-owner",
            "--no-privileges",
            "--exit-on-error",
        ]
    )
    if postgres_clean:
        cmd.extend(["--clean", "--if-exists"])
    stream_archive_member_to_cmd(archive_file, member, cmd, dry_run=dry_run)


def restore_postgres_external(
    archive_file: Path,
    member: str,
    host: str,
    port: str,
    db_user: str,
    db_password: str,
    db_name: str,
    postgres_clean: bool,
    dry_run: bool,
) -> None:
    common.ensure_tool_exists("pg_restore")
    cmd = [
        "pg_restore",
        "-h",
        host,
        "-p",
        str(port),
        "-U",
        db_user,
        "-d",
        db_name,
        "--no-owner",
        "--no-privileges",
        "--exit-on-error",
    ]
    if postgres_clean:
        cmd.extend(["--clean", "--if-exists"])
    env = os.environ.copy()
    if db_password:
        env["PGPASSWORD"] = db_password
    stream_archive_member_to_cmd(archive_file, member, cmd, env=env, dry_run=dry_run)


def restore_redis_in_container(
    archive_file: Path,
    member: str,
    container_id: str,
    service_name: str,
    base_cmd: List[str],
    rdb_path_in_container: str,
    no_redis_restart: bool,
    dry_run: bool,
) -> None:
    with tempfile.TemporaryDirectory(prefix="restore_redis_") as tmp_dir:
        tmp_file = Path(tmp_dir) / "restore.rdb"
        extract_archive_member_to_file(archive_file, member, tmp_file, dry_run=dry_run)

        cp_cmd = ["docker", "cp", str(tmp_file), f"{container_id}:{rdb_path_in_container}"]
        if dry_run:
            print(f"[dry-run] {common.cmd_to_log_string(cp_cmd)}")
        else:
            common.run_cmd(cp_cmd)

        if no_redis_restart:
            print("Redis 数据文件已覆盖，但未重启 Redis（--no-redis-restart）")
            return

        restart_cmd = base_cmd + ["restart", service_name]
        if dry_run:
            print(f"[dry-run] {common.cmd_to_log_string(restart_cmd)}")
        else:
            common.run_cmd(restart_cmd)


def restore_redis_external(
    archive_file: Path,
    member: str,
    redis_external_rdb_path: Optional[str],
    redis_external_restart_cmd: Optional[str],
    dry_run: bool,
) -> None:
    if not redis_external_rdb_path:
        raise BackupError(
            "检测到 Redis 外部模式恢复。请提供 --redis-external-rdb-path，或使用 --no-redis 手工恢复。"
        )
    target = Path(redis_external_rdb_path).resolve()
    extract_archive_member_to_file(archive_file, member, target, dry_run=dry_run)
    if redis_external_restart_cmd:
        restart_cmd = shlex.split(redis_external_restart_cmd)
        if dry_run:
            print(f"[dry-run] {common.cmd_to_log_string(restart_cmd)}")
        else:
            common.run_cmd(restart_cmd)


def parse_args() -> argparse.Namespace:
    script_dir = Path(__file__).resolve().parent
    parser = argparse.ArgumentParser(description="根据 tar.zst 备份包恢复 PostgreSQL 和 Redis")
    parser.add_argument(
        "-a",
        "--archive-file",
        required=True,
        help="备份压缩包路径（*.tar.zst）",
    )
    parser.add_argument(
        "-f",
        "--compose-file",
        action="append",
        dest="compose_files",
        help="docker compose 文件路径，可重复指定。默认 deploy/docker-compose.yml",
    )
    parser.add_argument(
        "-e",
        "--env-file",
        default=str(script_dir / ".env"),
        help="环境变量文件路径，默认 deploy/.env",
    )
    parser.add_argument(
        "--project-directory",
        default=str(script_dir),
        help="docker compose project directory，默认 deploy/",
    )
    parser.add_argument(
        "--project-name",
        default=None,
        help="docker compose project name（等价 -p）。默认优先读取 .env 的 COMPOSE_PROJECT_NAME",
    )
    parser.add_argument("--postgres-service", default=None, help="显式指定 PostgreSQL 服务名")
    parser.add_argument("--redis-service", default=None, help="显式指定 Redis 服务名")
    parser.add_argument("--no-postgres", action="store_true", help="跳过 PostgreSQL 恢复")
    parser.add_argument("--no-redis", action="store_true", help="跳过 Redis 恢复")
    parser.add_argument(
        "--postgres-clean",
        action="store_true",
        default=True,
        help="恢复前清理目标对象（默认开启）",
    )
    parser.add_argument(
        "--no-postgres-clean",
        action="store_false",
        dest="postgres_clean",
        help="不执行 pg_restore --clean/--if-exists",
    )
    parser.add_argument(
        "--redis-rdb-path",
        default="/data/dump.rdb",
        help="Redis 容器内 RDB 目标路径，默认 /data/dump.rdb",
    )
    parser.add_argument(
        "--no-redis-restart",
        action="store_true",
        help="覆盖 RDB 后不自动重启 Redis（默认会重启）",
    )
    parser.add_argument(
        "--redis-external-rdb-path",
        default=None,
        help="外部 Redis 模式下，RDB 目标路径（本机可写路径）",
    )
    parser.add_argument(
        "--redis-external-restart-cmd",
        default=None,
        help="外部 Redis 模式下，恢复后执行的重启命令（可选）",
    )
    parser.add_argument("--dry-run", action="store_true", help="仅打印恢复计划，不执行恢复")
    parser.add_argument(
        "--force",
        action="store_true",
        help="确认执行恢复（非 dry-run 场景必须提供）",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    if not args.dry_run and not args.force:
        raise BackupError("恢复是高风险操作。请添加 --force 以确认执行。")

    script_dir = Path(__file__).resolve().parent
    archive_file = Path(args.archive_file).resolve()
    compose_files = [Path(p).resolve() for p in (args.compose_files or [script_dir / "docker-compose.yml"])]
    env_file = Path(args.env_file).resolve()
    project_dir = Path(args.project_directory).resolve()

    if not archive_file.exists():
        raise BackupError(f"备份包不存在: {archive_file}")
    if archive_file.suffix != ".zst":
        raise BackupError(f"备份包后缀必须为 .zst: {archive_file}")
    for item in compose_files:
        if not item.exists():
            raise BackupError(f"compose 文件不存在: {item}")

    env_map = common.parse_env_file(env_file)
    project_name = args.project_name or env_map.get("COMPOSE_PROJECT_NAME") or None
    base_cmd = common.compose_base_cmd(compose_files, env_file, project_dir, project_name)
    compose_cfg = common.load_compose_config(base_cmd)
    services: Dict[str, Dict[str, Any]] = compose_cfg.get("services", {})
    if not services:
        raise BackupError("compose 配置中未找到 services")

    members = list_archive_members(archive_file)
    meta_member = find_metadata_member(members)
    meta = read_archive_member_json(archive_file, meta_member)
    backup_dir_name = derive_backup_dir_name(meta, meta_member)

    pg_meta = meta.get("postgres") or {}
    redis_meta = meta.get("redis") or {}
    pg_member = resolve_data_member(pg_meta, backup_dir_name)
    redis_member = resolve_data_member(redis_meta, backup_dir_name)

    if not args.no_postgres:
        if not pg_member:
            raise BackupError("备份包元数据中没有 PostgreSQL 备份文件路径")
        pg_service = common.choose_postgres_service(services, args.postgres_service)
        pg_service_env = common.normalize_environment(services.get(pg_service, {}).get("environment")) if pg_service else {}
        fallback_user, fallback_password, fallback_db = common.postgres_creds(pg_service_env, env_map)
        pg_user = str(pg_meta.get("user") or fallback_user)
        pg_db = str(pg_meta.get("database") or fallback_db)
        pg_password = fallback_password

        if pg_service:
            pg_container = (
                common.dry_run_container_ref(services, pg_service)
                if args.dry_run
                else common.get_compose_container_id(base_cmd, pg_service)
            )
            restore_postgres_in_container(
                archive_file=archive_file,
                member=pg_member,
                container_id=pg_container,
                db_user=pg_user,
                db_password=pg_password,
                db_name=pg_db,
                postgres_clean=args.postgres_clean,
                dry_run=args.dry_run,
            )
            print(f"PostgreSQL 恢复目标: {pg_service} ({pg_db})")
        else:
            db_host, db_port = common.external_db_conn(env_map)
            restore_postgres_external(
                archive_file=archive_file,
                member=pg_member,
                host=db_host,
                port=db_port,
                db_user=pg_user,
                db_password=pg_password,
                db_name=pg_db,
                postgres_clean=args.postgres_clean,
                dry_run=args.dry_run,
            )
            print(f"PostgreSQL 恢复目标: {db_host}:{db_port}/{pg_db}")

    if not args.no_redis and redis_meta:
        if not redis_member:
            raise BackupError("备份包元数据中没有 Redis 备份文件路径")
        redis_service = common.choose_redis_service(services, args.redis_service)
        if redis_service:
            redis_container = (
                common.dry_run_container_ref(services, redis_service)
                if args.dry_run
                else common.get_compose_container_id(base_cmd, redis_service)
            )
            restore_redis_in_container(
                archive_file=archive_file,
                member=redis_member,
                container_id=redis_container,
                service_name=redis_service,
                base_cmd=base_cmd,
                rdb_path_in_container=args.redis_rdb_path,
                no_redis_restart=args.no_redis_restart,
                dry_run=args.dry_run,
            )
            print(f"Redis 恢复目标: {redis_service} ({args.redis_rdb_path})")
        else:
            restore_redis_external(
                archive_file=archive_file,
                member=redis_member,
                redis_external_rdb_path=args.redis_external_rdb_path,
                redis_external_restart_cmd=args.redis_external_restart_cmd,
                dry_run=args.dry_run,
            )
            print("Redis 恢复目标: 外部模式")

    print("恢复流程完成")
    return 0


if __name__ == "__main__":
    try:
        raise SystemExit(main())
    except BackupError as exc:
        print(f"[ERROR] {exc}", file=sys.stderr)
        raise SystemExit(1)
